
public class Pixel {
	private int colorR;
	private int colorG;
	private int colorB;
	
	public int getColorR() {
		return this.colorR;
	}
	public void setColorR(int colorR) {
		this.colorR = colorR;
	}
	public int getColorG() {
		return this.colorG;
	}
	public void setColorG(int colorG) {
		this.colorG = colorG;
	}
	public int getColorB() {
		return this.colorB;
	}
	public void setColorB(int colorB) {
		this.colorB = colorB;
	}
}
